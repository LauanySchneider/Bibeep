<h1> Sobre: </h1>
    
   <p>🚀 Bibeep - Cadastro de códigos de barras
    Seja bem-vindo ao Bibeep! Este projeto foi criado para permitir o cadastro de códigos de barras e informações de produtos e a exibição de uma lista com todos os dados cadastrados, tudo armazenado em um banco de dados. Com ele, você pode cadastrar informações como Código do Produto, Marca, Nome do Produto, Cor, Quantidade, Valor Unitário e Margem de Lucro, e ver a lista de Produtos com seu Valor Total e Valor de Venda em tempo real com foto. 😎 </p> 
    
    <p>👤 Autor
    Este projeto foi desenvolvido por [Lauany Schneider Souza & Murilo Garcia Ribeiro]. ✨ </p>
    
    📋 Funcionalidades
    Cadastro de Produto 📝
    <br>
   <p>• Adicione informações de um novo produto: Código do Produto, Marca, Nome do Produto, Cor, Quantidade, Valor Unitário e Margem de Lucro. O sistema valida se todos os campos foram preenchidos corretamente. Após a validação, o sistema calcula o Valor Total e o Valor de Venda e adiciona no bando de dados. </p> 
    
    Listagem de Produtos 📊
    <br>
    <p>• Visualize todos os produtos cadastrados em uma tabela organizada. As informações são compartilhadas diretamente do banco de dados. Você pode atualizar a lista a qualquer momento para visualizar novos usuários.</p>
    
    Exclusão de Produtos 🗑️
    
    <p> • Exclua produtos da lista com um simples clique. A exclusão é feita no Banco de Dados e a tabela é atualizada automaticamente.</p>
    
    🛠️ Tecnologias Utilizadas
    <p> • HTML: Estruturação da página de cadastro e administração. • CSS: Estilização das páginas, utilizando fontes e núcleos para uma interface agradável. • JavaScript: Lógica de controle de formulários, validação de dados e manipulação. • Bootstrap: Framework para estilizar componentes como barra de navegação e botões. • Banco de Dados: Armazenamento local dos dados do usuário no navegador. • FrameWork Laravel: Biblioteca PHP.</p>
    
    📂 Estrutura de Arquivos
    Aqui os principais arquivos do projeto estão:
    
    <p> • index.html: Página de cadastro de usuários. • admin.html: Página de administração para exibir os usuários cadastrados. • userCreation.js: Lógica para adicionar usuários ao localStorage e validar os dados. • admin.js: Lógica para listar e excluir usuários. • style.css: Estilos gerais do projeto. • index.css: Estilos específicos da página de cadastro.</p>
    
    🚀 Como Usar
    <p> Clone ou Baixe o Repositório Você pode clonar o repositório ou baixar como um arquivo zip:
    git clone https://github.com/seu-usuario/nome-do-projeto.git </p>
    
    <p>Abra o Projeto no Navegador Após clonar o repositório, abra o arquivo index.html no seu navegador. A página de cadastro estará pronta para ser usada! </p>
    
    <p>Cadastro de Produtos Preencha os campos de cadastro com as informações do produto. Clique no botão "Adicionar Produto" para salvar. Caso algum campo não seja preenchido ou esteja inválido, o sistema exibirá um alerta. </p>
    
    <p>Listagem de Produtos Para visualizar os usuários cadastrados, clique no botão "Listagem de Produtos". Uma lista de produtos será exibida e você poderá atualizar a tabela sempre que desejar. Para excluir um produto, basta clicar no botão “Excluir” ao lado do nome do produto. </p>
    
